//1.__proto__: JavaScriptda har bir obyekt __proto__ xususiyatiga ega. Bu xususiyat obyektning prototip zanjirini tashkil etuvchi boshqa obyektga havola qiladi.

//2.prototip: JavaScript funksiyasi yaratilganda, ushbu funksiyaning prototip xususiyati yaratiladi. Bu xususiyat ushbu funktsiya tomonidan yaratiladigan yangi ob'ektlarning xossalari va usullarini aniqlash uchun ishlatiladi.

//3.instance obyekti (instanciya obyekt): Sinflarning namunasi bo'lgan ob'ekt. Misol uchun, sinf new kalit so'zi bilan chaqirilganda, yaratilgan ob'ekt instanciya obyekt sifatida hisoblanadi.

//4.Global ob'ekt (global obyekt): JavaScript ish vaqti davomida doimo mavjud bo'lgan ob'ekt. Brauzerda bu ob'ekt window deb ataladi. Ushbu ob'ekt global o'zgaruvchilar va funktsiyalar kabi boshqa global xususiyatlarni o'z ichiga oladi.

//5.Polymorphism: Polimorfizm - bu ob'ektning turli kontekstlarda o'zini boshqacha tutishi. JavaScript-da polimorfizmga odatda meros orqali erishiladi.

//6.Abstraktsiya: Abstraktsiya - bu ob'ektning faqat muhim xususiyatlari yoki xatti-harakatlariga urg'u berish va keraksiz tafsilotlarni yashirish. Bu murakkab tizimlarni boshqarishga imkon beradi.

//7.Incapsulation (enkapsulyatsiya): Inkapsulyatsiya - bu ob'ektning ichki holatini va xatti-harakatlarini boshqa ob'ektlardan yashirish. Bu ob'ektning ichki amalga oshirish tafsilotlarini tashqaridan manipulyatsiya qilishni oldini oladi.

//8.Inheritance: Inheritance - bu sinf boshqa sinfdan xususiyatlar va xatti-harakatlarni meros qilib olishidir. Bu kodning qayta ishlatilishini oshiradi.

//9.Getter / Setter: Getter va setter - bu ob'ekt xususiyatlariga kirish yoki ushbu xususiyatlarning qiymatini o'rnatish uchun ishlatiladigan maxsus funktsiyalar. Getter xususiyat qiymatini o'qish uchun ishlatiladi, sozlagich esa xususiyat qiymatini o'rnatish uchun ishlatiladi.

//10.Constrctor function funksiyasi (konstruktor funksiyasi): Konstruktor funksiyasi JavaScript funksiyasi bo'lib, u tur sifatida ishlaydi va yangi obyektlarni yaratish uchun ishlatiladi. Yangi ob'ekt yaratilganda, bu funktsiya chaqiriladi va ushbu funktsiya doirasida ob'ektning xususiyatlari aniqlanadi.





